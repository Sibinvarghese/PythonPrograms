var arr=[10,20,30,40]

console.log(arr)   // printing all array values

console.log(arr[0]) //index wise calling

for(num of arr){
console.log(num)    //one by one printing
}


arr.push(50)   //push used for adding elements
console.log(arr)


arr.pop()   //pop used for deleting elements
console.log(arr)